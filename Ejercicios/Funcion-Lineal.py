def funcion_lineal(x): # Tome como argumento un valor x

    m = 2 #Pendiente de la linea
    b = 3 #Punto de interseccion en el eje y
    y = m*x + b # Formula para una funcion lineal

    return y # Guardar la informacion de la varible y

resultado = funcion_lineal(4) # Puede manipular el valor de 4 = x, osea x puede ser cualquier numero que usted quiere en este caso puse 4 como argumento, pero puede utilizar otros.
print("El valor de 'y' + es: ", resultado)